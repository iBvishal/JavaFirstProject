package com.sapient;

public class POJO_Student {
	private int id;
	private String name;
	private String city;
	
    public int getId() {
        return id;
    }
    public void setId(final int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(final String city) {
        this.city = city;
    }
}
